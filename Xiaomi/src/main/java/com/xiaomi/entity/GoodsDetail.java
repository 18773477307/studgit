package com.xiaomi.entity;

public class GoodsDetail {
	private String goodsName;
	private String goodsPics;
	private String goodsProcessor;
	private String goodsMemory;
	private String goodsCell;
	private String goodsPlaypic;
	private String goodsCan;
	
	
	
	public String getGoodsName() {
		return goodsName;
	}
	
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	
	public String getGoodsPics() {
		return goodsPics;
	}
	
	public void setGoodsPics(String goodsPics) {
		this.goodsPics = goodsPics;
	}
	
	public String getGoodsProcessor() {
		return goodsProcessor;
	}
	
	public void setGoodsProcessor(String goodsProcessor) {
		this.goodsProcessor = goodsProcessor;
	}
	
	public String getGoodsMemory() {
		return goodsMemory;
	}
	
	public void setGoodsMemory(String goodsMemory) {
		this.goodsMemory = goodsMemory;
	}
	
	public String getGoodsCell() {
		return goodsCell;
	}
	
	public void setGoodsCell(String goodsCell) {
		this.goodsCell = goodsCell;
	}
	
	public String getGoodsPlaypic() {
		return goodsPlaypic;
	}
	
	public void setGoodsPlaypic(String goodsPlaypic) {
		this.goodsPlaypic = goodsPlaypic;
	}
	
	public String getGoodsCan() {
		return goodsCan;
	}
	
	public void setGoodsCan(String goodsCan) {
		this.goodsCan = goodsCan;
	}

	public GoodsDetail(String goodsName, String goodsPics,
			String goodsProcessor, String goodsMemory, String goodsCell,
			String goodsPlaypic, String goodsCan) {
		super();
		this.goodsName = goodsName;
		this.goodsPics = goodsPics;
		this.goodsProcessor = goodsProcessor;
		this.goodsMemory = goodsMemory;
		this.goodsCell = goodsCell;
		this.goodsPlaypic = goodsPlaypic;
		this.goodsCan = goodsCan;
	}

	public GoodsDetail() {
		super();
	}

	@Override
	public String toString() {
		return "GoodsDetail [goodsName=" + goodsName + ", goodsPics="
				+ goodsPics + ", goodsProcessor=" + goodsProcessor
				+ ", goodsMemory=" + goodsMemory + ", goodsCell=" + goodsCell
				+ ", goodsPlaypic=" + goodsPlaypic + ", goodsCan=" + goodsCan
				+ "]";
	}
	
}
